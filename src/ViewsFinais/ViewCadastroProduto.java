    public boolean verifica() {
        if (this.jTextFieldNomeProduto.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Preencha o campo Nome Setor");
            this.jTextFieldNomeProduto.requestFocus();
            return false;
        }
        if (this.jTextFieldPeso.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Preencha o campo Nome Setor");
            this.jTextFieldPeso.requestFocus();
            return false;
        }
        if (this.jComboBoxTipo.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Selecione o campo Setor");
            this.jComboBoxTipo.requestFocus();
            return false;
        }
        if (this.jComboBoxCategoria.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Selecione o campo Servi√ßo");
            this.jComboBoxCategoria.requestFocus();
            return false;
        }
        return true;
    }

